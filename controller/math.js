module.exports = {
  pertambahan: function (num_1, num_2) {
    return num_1 + num_2
  },
  perkurangan: function (num_1, num_2) {
    return num_1 - num_2
  },
  pembagian: function (num_1, num_2) {
    return num_1 / num_2
  },
  perkalian: function (num_1, num_2) {
    return num_1 * num_2
  }
}
