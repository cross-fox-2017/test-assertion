module.exports = {
  pertambahan : function(a,b){
    return a+b
  },
  pengurangan : function(a,b){
    return a-b
  },
  perkalian : function(a,b){
    return a*b
  },
  pembagian : function(a,b){
    return a/b
  },
  pangkatdua : function(num){
    return Math.pow(num,2)
  },
  akardari : function(num){
    return Math.sqrt(num)
  }
}
